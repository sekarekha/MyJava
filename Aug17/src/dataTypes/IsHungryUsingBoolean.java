package dataTypes;

public class IsHungryUsingBoolean {
    public static void main(String args[]) {
        boolean isHungry = true;
        if (isHungry) {
            System.out.println("Yes,I am hungry");
            System.out.println("Well,I should go grab a bite to eat.");
        } else {
            System.out.println("No,not really");
        }
    }
}
